<?php

namespace App\Rules;

use Illuminate\Contracts\Validation\Rule;

class PastBenefits implements Rule
{
    /**
     * Create a new rule instance.
     *
     * @return void
     */
    public function __construct()
    {
        //
    }

    /**
     * Determine if the validation rule passes.
     *
     * @param  string  $attribute
     * @param  mixed  $value
     * @return bool
     */
    public function passes($attribute, $value)
    {
        //
        if($value=='UPSC')
        {
            return false;
        }
        else
        {
            return true;
        }
    }

    /**
     * Get the validation error message.
     *
     * @return string
     */
    public function message()
    {
        return 'Your form has been rejected, as you have taken the benefit of BARTI’s UPSC coaching scheme earlier. ';
    }
}
